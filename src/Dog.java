public class Dog {
    String Breed;
    private String Color;
    private int Age;

    public void setBreed(String vBreed){
        this.Breed = vBreed;
    }
    public void setColor(String vColor){
        this.Color = vColor;
    }
    public void setAge(int vAge) {
        this.Age = vAge;
    }
    public String getBreed(){
        return Breed;
    }
    public String getColor(){
        return Color;
    }
    public int getAge(){
        return Age;
    }


}

